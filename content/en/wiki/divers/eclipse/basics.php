		<ul>
			<li>
				<h2>Change layout</h2>
				<ul>
					<li>
						You can drag and drop any "Tab" to any highlghted location in the Eclipse.
					</li>
					<li>
						To save view: Window -> perspectives -> "save perspectives".
					</li>
					<li>
						To reset view: Window -> perspectives -> "reset perspectives".
					</li>
				</ul>
			</li>
			<li>
				<h2>Detach one  project from many on workspace</h2>
				<ul>
					<li>
						When you have many projects in workspace, you can right-click a project -> "Go in".
					</li>
					<li>
						To come back you can use the yellow left arrow on top of workspace area.
					</li>
				</ul>
			</li>
			<li>
				<h2>Link editor files and package explorer</h2>
				<ul>
					<li>
						Click "View menu" (the down arrow in top-right package explorer) -> "Link with editor".
					</li>
				</ul>
			</li>
			<li>
				<h2>Build automatically</h2>
				<ul>
					<li>
						Top menu bar -> "Project" -> "Build automatically".
					</li>
				</ul>
			</li>
			<li>
				<h2>Quick access</h2>
				<ul>
					<li>
						Top menu bar -> input text on top right.
						<ul>
							<li>
								Tape any command and see result.
							</li>
						</ul>
					</li>
				</ul>
			</li>
			<li>
				<h2>Local history</h2>
				<ul>
					<li>
						Edit a file the save it.
					</li>
					<li>
						Right-click a project -> "show local history" -> select date
					</li>
					<li>
						You'll come back to the version saved on that date.
					</li>
					<li>
						To configure local history: Quick access -> "lh"
					</li>
				</ul>
			</li>
			<li>
				<h2>Show hidden files</h2>
				<ul>
					<li>
						Window -> View -> "Nivigator view".
					</li>
				</ul>
			</li>
			<li>
				<h2>Working sets</h2>
				<ul>
					<li>
						Select files -> "view menu" icon -> select working sets.
					</li>
					<li>
						Then you can search (using Ctrl+F) only in this working sets.
					</li>
				</ul>
			</li>
			<li>
				<h2>Tasks and bookmarks</h2>
				<ul>
					<li>
						Way1: Add //TODO: some text
					</li>
					<li>
						Way2: Select a block of code -> right-click on marker bar -> "Add task".
					</li>
				</ul>
			</li>
			<li>
				<h2>Update commands</h2>
				<ul>
					<li>
						"window" -> "preferences" -> tap "keys" to filter.
					</li>
					<li>
						Tap "Ctrl+F" for example and see associated commands.
					</li>
					<li>
						Change command key:
						<ul>
							<li>
								select command -> "unbind command" -> fill "binding" text.
							</li>
						</ul>
					</li>
				</ul>
