    <div class="wiki-article-body invisible">
        <ul>
            <li>REPL: Read Eval Print Loop</li>
            <li>
                It represents a computer environment like a window console or
                Unix/Linux shell where a command is entered and system responds with an output in interactive mode.
            </li>
            <li>
                Node.js or Node comes bundled with a REPL environment.
            </li>
            <li>
                It performs the following desired tasks.
                <ul>
                    <li>Read: Reads user's input, parse the input into JavaScript data-structure and stores in memory.
                    </li>
                    <li>Eval: Takes and evaluates the data structure.</li>
                    <li>Print: Prints the result.</li>
                    <li>Loop: Loops the above command until user press ctrl-c twice.</li>
                </ul>
            </li>
        </ul>
    </div>
