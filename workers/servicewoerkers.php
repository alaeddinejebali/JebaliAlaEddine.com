				<h2>How services workers work?</h2>
				<ul>
					<li>
						Without service worker
						<ul>
							<li>
								A browser send a request to a server.
							</li>
							<li>
								The server send a response to the browser.
							</li>
						</ul>
					</li>
					<li>
						With a service worker
						<ul>
							<li>
								When there is an internet connection
								<ul>
									<li>
										A browser send a request to the service worker.
									</li>
									<li>
										The service worker send the request to the server
									</li>
									<li>
										The server send a response to the service worker.
									</li>
									<li>
										The service worker send the response to the browser.
									</li>
								</ul>
							</li>
							<li>
								When there is no internet connection
								<ul>
									<li>
										A browser send a request to a service worker.
									</li>
									<li>
										The service worker send a response to the browser.
									</li>
