			<li>
				<h2>MVC pattern</h2>
				<ul>
					<li>Its goal is that of providing a methodology for separating the
						business logic (model) from the display logic (view) and the
						decisional controls (controller).</li>
					<li>Process:
						<ol>
							<li>User initiates an action by callingthe Controller.</li>
							<li>Controller interfaces with the Model (causing it to perform
								some sort of action and, therefore, changing its state).</li>
							<li>View is called, thus causing the user interface to be
								refreshed to reflect the changes in the Model.</li>
						</ol>
					</li>
				</ul>
			</li>
		</ul>
