				<li>
					<h2>Active Record pattern</h2>
					<ul>
						<li>This pattern is commonly used by object persistence tools and
							in object-relational mapping (ORM).</li>
						<li>The active record pattern is an approach to accessing data in a
							database.</li>
						<li>A database table or view is wrapped into a class. Thus, an
							object instance is tied to a single row in the table. After
							creation of an object, a new row is added to the table upon save.
							Any object loaded gets its information from the database. When an
							object is updated the corresponding row in the table is also
							updated. The wrapper class implements accessor methods or
							properties for each column in the table or view.</li>
					</ul>
				</li>
			</ul>
