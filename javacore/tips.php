					<h2>History of Java</h2>
					<ul>
						<li>
							Java was developed by <a href="https://en.wikipedia.org/wiki/James_Gosling" target="_blank">James Ghosling</a>, <a href="https://en.wikipedia.org/wiki/Patrick_Naughton" target="_blank">Patrick Naughton</a>, Mike Sheridan at <a href="https://en.wikipedia.org/wiki/Sun_Microsystems" target="_blank">Sun Microsystems</a> in 1991.
						</li>
						<li>
							It took 18 months to develop the first working version: first demonstration was in summer 1992.
						</li>
						<li>
							The initial name was <strong>Oak</strong> but it was renamed to Java in 1995.
							This is because <strong>Oak</strong> was a registered trademark of another Tech company: <a href="https://en.wikipedia.org/wiki/Oak_Technology" target="_blank">Oak Technology</a>.
						</li>
					</ul>
