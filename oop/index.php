<ul>
	<li>
		<h2>Polymorphism</h2>
		<ul>
			<li>
				This is an object oriented concept where same function can be used for different purposes. For example function name will remain same but it make take different number of arguments and can do different task.
			</li>
		</ul>
	</li>
	<li>
		<h2>Encapsulation</h2>
		<ul>
			<li>
				encapsulation is the hiding of data implementation by restricting access to accessors and mutators. 
			</li>
		</ul>
	</li>
	<li>
		<h2>Abstraction</h2>
		<ul>
			<li>
				Abstraction is used to manage complexity. Software developers use abstraction to decompose complex systems into smaller components.
			</li>
		</ul>
	</li>
	<li>
		<h2>Object</h2>
		<ul>
			<li>
				An individual instance of the data structure defined by a class. You define a class once and then make many objects that belong to it. Objects are also known as instance.
			</li>
		</ul>
	</li>
	<li>
		<h2>Private members</h2>
		<ul>
			<li>
				The member is only  accessible by the class in which it is declared.
			</li>
		</ul>
	</li>
	<li>
		<h2>Protected members</h2>
		<ul>
			<li>
				A protected property or method is only accessible in the class in which it is declared, as well as in classes that extend that class.
			</li>
		</ul>
	</li>
	<li>
		<h2>Abstract class</h2>
		<ul>
			<li>
				An abstract class is one that cannot be instantiated, only inherited. 
			</li>
			<li>
				Function definitions inside an abstract class must also be abstract. It is not legal to have abstract function definitions inside a non-abstract class.
			</li>
		</ul>
	</li>
</ul>
